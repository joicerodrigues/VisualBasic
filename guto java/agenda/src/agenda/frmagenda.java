    private void jbtnnovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnnovoActionPerformed
        // TODO add your handling code here:
        
        //variaveis
        String nome, celular, telefone, operadora, data, email, sexo;
        
        //atribuir os valores das caixas para as variaveis
           nome = jtxtnome.getText();
           celular = jtxtcelular.getText();
           telefone = jtxttelefone.getText();
           email = jtxtemail.getText();
           data = jtxtdata.getText();
           
           operadora = jcmdoperadora.getSelectedItem().toString();
           sexo = jcmdsexo.getSelectedItem().toString();
           
           //adiciona uma nova linha na tabela
           DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();
           modelo.addRow(new Object[]{nome, data, email, sexo, telefone, celular, operadora});
           
           
           //limpar os campos
           jtxtnome.setText("");
           jtxtcelular.setText("");
           jtxtemail.setText("");
           jtxttelefone.setText("");
           jtxtdata.setText("");
           
           jcmdoperadora.setSelectedIndex(0);
           jcmdsexo.setSelectedIndex(0);
    }//GEN-LAST:event_jbtnnovoActionPerformed

    private void jbtnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnexcluirActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();
        //se foi selecionada alguma linha do jtable
        
        if (jTable.getSelectedRow() >- 0) {
            modelo.removeRow(jTable.getSelectedRow());  
    }//GEN-LAST:event_jbtnexcluirActionPerformed
  else{
            JOptionPane.showMessageDialog(null, "linha n√£o selecionada");
        }
    }   
    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        // TODO add your handling code here:
        
        if (evt.getClickCount() == 2)
        {
            int linha = jTable.getSelectedRow();
            //add os valores nas caixas de texto
            jtxtnome.setText(jTable.getValueAt(linha, 0).toString());
            jtxtdata.setText(jTable.getValueAt(linha, 1).toString());
            jtxtemail.setText(jTable.getValueAt(linha, 2).toString());
            jcmdsexo.setSelectedItem(jTable.getValueAt(linha, 3).toString());
            jtxttelefone.setText(jTable.getValueAt(linha, 4).toString());
            jtxtcelular.setText(jTable.getValueAt(linha, 5).toString());
            jcmdoperadora.setSelectedItem(jTable.getValueAt(linha, 6).toString());
            
            // armazena a linha
            linhaselecionada = linha;
            
        }
        
    }//GEN-LAST:event_jTableMouseClicked

